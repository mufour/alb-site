                <h3>AMICALE EN DANGER !!</h3>
                <p>
                    L’Amicale Laïque connaît des difficultés de trésorerie depuis plus de 15 ans. Des audits
                    réalisés par des cabinets indépendants à la demande de la Mairie de Bordeaux en 2010 et
                    2024, ont chaque fois conclu à l’insuffisance de subventionnement des missions sociales
                    réalisées par l’association. Le recours à la négociation de découverts bancaires ou à des
                    prêts d’administrateur, avaient jusque-là permis de franchir dans la douleur chacune des
                    crises rencontrées.
                <h4>L’Amicale risque de fermer ses portes dans quelques semaines</h4>
                <p>
                    Nos efforts de gestion&nbsp;: réduction de personnel, réduction de charges générales, arrêt
                    des investissements et arrêt de l’événementiel, ne peuvent compenser la baisse globale
                    et drastique de subventions pour nos actions&nbsp;: moins 85000€ en 2025 par rapport à 2023
                    et moins 100000€ prévus en 2026 par rapport à 2023.
                    Pourtant la demande sociale a explosé ces dernières années. La population a doublé avec
                    la construction des Bassins à flot. La demande de places d’accompagnement scolaire
                    augmente et les listes d’attente s’allongent. Le nombre de personnes en difficulté s’est
                    accru depuis la période du Covid. Nous accompagnons désormais 4500 d’entre elles par
                    an en moyenne, pour l’aide administrative (1200 seulement en 2019). Le nombre
                    d’adhérents à nos ateliers a également augmenté, sans que nous puissions en développer
                    de nouveaux.
                    De fait, l’Amicale Laïque est aujourd’hui dans un état critique, qui peut conduire à une
                    cessation d’activité dès février 2026, si l’association ne parvient pas à lever 100000€
                    rapidement.</p>
                <h4>Nous avons besoin de tous&nbsp;!</h4>
                <p>
                    Les semaines qui viennent vont être cruciales. Au-delà du symbole que constituerait la
                    disparition de l’Amicale Laïque après 144 années d’existence, ce sont ses finalités qui
                    vous manqueraient que vous soyez usagers, adhérents, amis et habitants.
                    Si vous jugez utiles les actions que nous exerçons, si elles vous sont nécessaires, si vous
                    pensez que l’Amicale doit vivre, nous vous proposerons des initiatives de soutien et de
                    mobilisation, pour sensibiliser les pouvoirs publics voir de potentiels donateurs, afin
                    d’obtenir les moyens de poursuivre notre action.
